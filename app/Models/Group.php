class Group extends Model
{
    use SoftDeletes;

    public $table = 'groups';
    

    protected $dates = ['deleted_at'];


    public $fillable = [
        'name',
        'image',
        'members_counts',
        'status'
    ];

    /**
     * The attributes that should be casted to native types.
     *
     * @var array
     */
    protected $casts = [
        'name' => 'string',
        'image' => 'string',
        'members_counts' => 'integer',
        'status' => 'integer'
    ];

    /**
     * Validation rules
     *
     * @var array
     */
    public static $rules = [
        
    ];

    public function members()
    {
        return $this->belongsToMany('App\Models\Profile','group_users')->withPivot('status');
        //return $this->hasMany('App\Models\Profile', 'profile_id', 'group_id','group_users');
    }
    
    public function groupUsers()
    {
    	return $this->hasMany('App\Models\GroupUser');
    	//return $this->hasMany('App\Models\Profile', 'profile_id', 'group_id','group_users');
    }
    
}
